/*题目描述 
在某个星球，每个人身上都有不一样的标号。一天，小娟在食堂排队时，发现后面老是喜欢插队，她看到很生气，
拿出一张魔法卡，巴拉巴拉能量（魔仙变身，看多了动画片，hhh）。。。。。。。，将他们全部按照从小到大
按照标号排好了。你能帮忙写出魔法卡内部的程序吗？写出来就给你一个气球。

输入描述:
第一行，输入一个整数n(1<=n<=100000);
第二行，输出n个整数；
注意多组输入
输出描述:
输出n个数*/
#include<bits/stdc++.h>
using namespace std;
int main()
{
	int n,a[100007]={0};
	cin>>n;
	for(int h=0;h<n;h++) cin>>a[h];
	sort(a,a+n);
	for(int h=0;h<n;h++) printf("%d ",a[h]);
	return 0;
}
