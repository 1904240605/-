/*
描述
奶牛Bessie计划好好享受柔软的春季新草。新草分布在R行C列的牧场里。它想计算一下牧场中的草丛数量。
在牧场地图中，每个草丛要么是单个“#”，要么是有公共边的相邻两个“#”。给定牧场地图，
计算有多少个草丛。
例如，考虑如下5行6列的牧场地图
.#....
..#...
..#..#
...##.
.#....
这个牧场有5个草丛：一个在第一行，一个在第二列横跨了二、三行，一个在第三行，一个在第四行横跨
了四、五列，最后一个在第五行。

输入
第一行包含两个整数R和C，中间用单个空格隔开。
接下来R行，每行C个字符，描述牧场地图。字符只有“#”或“.”两种。(1 <= R, C <= 100 )
输出
输出一个整数，表示草丛数。
*/
#include <iostream>
using namespace std;
int main()
{
    int R, C, x=0, i, j, k, dx[4]={0,1,-1,0}, dy[4]={1,0,0,-1};
    char A[102][102]={0};
    cin >> R >> C;
    for (i=1; i<=R; i++)
        for (j=1; j<=C; j++)
            cin >> A[i][j];
    for (i=1; i<=R; i++)
        for (j=1; j<=C; j++){
            if (A[i][j] == '#'){
                x++;
                A[i][j] = '.';
                for (k=0; k<4; k++){
                    if (A[i+dx[k]][j+dy[k]] == '#')
                        A[i+dx[k]][j+dy[k]] = '.';
                }
            }
        }
    cout << x << endl;
    return 0;
}

